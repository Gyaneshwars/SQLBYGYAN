IF OBJECT_ID ('TEMPDB..#Finals') IS NOT NULL DROP TABLE #Finals
SELECT DISTINCT ProcessInstanceInstantiated,CASE WHEN KEYPERSON IS NULL AND TASKINSTANCEBEGUN IS NULL AND TASKINSTANCECOMPLETED IS NULL THEN 'ACTIVE' WHEN KEYPERSON IS NOT NULL AND TASKINSTANCEBEGUN IS NOT NULL AND TASKINSTANCECOMPLETED IS NULL THEN 'PAUSED'  ELSE 'COMPLETED' END AS JobStatus,PROCESSINSTANCEAPPIANID AS JobID,KEYPROCESSSTREAM,ProcessInstanceName AS JobName,STREAMNAME AS Process_Stream,
KEYPERSON,LOGINNAME AS JobCreator,SupervisorFullName,TASKINSTANCEBEGUN,TASKINSTANCECOMPLETED,PROCESSINSTANCETARGETDATE AS GoalDate, 
SNLANALYSTTASKENTRY INTO #Finals FROM #QueuedPF PIVOT (MAX(PROCESSFIELDVALUE) FOR FIELDIDENTIFIER IN
(PEO,ERD,DataItem,Industry,Assignee,MethodologyExclusions,CompanyName,Peak,CompanyId,emailSubject,fromName))AS PathPIVOT



SELECT * FROM #Finals WHERE JobStatus IN ('ACTIVE','PAUSED') ORDER BY ProcessInstanceInstantiated